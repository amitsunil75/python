import math
from math import sqrt,ceil,floor,factorial,fabs,copysign,gcd
print(sqrt(4.01))
# outout:200
print(ceil(4.01))
# output:5
print(floor(4.01))
# output:4
print(factorial(3))
# output:1*2*3=6
print(fabs(-33))
# output:absolute value of (-33) =33
print(copysign(-2,3))
# output :2
print(copysign(2,-3))
# output:-2
print(gcd(4))
# output:4
